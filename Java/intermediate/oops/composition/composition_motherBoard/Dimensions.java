package oops.composition_Motherboard;

/**
 * @author shreyas
 *
 */
public class Dimensions {

	private int height;
	private int weight;
	private int depth;

	public Dimensions(int height, int weight, int depth) {
		this.height = height;
		this.weight = weight;
		this.depth = depth;
	}

	public int getHeight() {
		return height;
	}

	public int getWeight() {
		return weight;
	}

	public int getDepth() {
		return depth;
	}

}
